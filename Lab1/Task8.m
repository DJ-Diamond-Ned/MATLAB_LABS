%Задание №17
%Реагирующая система состоит из следующих молекулярных видов:
%Al2O3; H3PO4; AlPO4; H2O; Fe2O3;  FePO4; NH3; NH4H2PO4.
%1.Задавшись следующими структурными видами: Al, P, N, H, O, Fe, 
%построить структурную матрицу, отражающую состав молекул веществ, определить её ранг и найти 
%соответствующие по размерности рангу невырожденные подматрицы, хотя бы две. 
%2.Определить реакции, которые могут протекать среди этих молекулярных видов и записать их.

%Al, P, N, H, O, Fe
M = [
    2 0 0 0 3 0;    %Al2O3 1
    0 1 0 3 4 0;    %H3PO4 2
    1 1 0 0 4 0;    %AlPO4 3
    0 0 0 2 1 0;    %H2O 4
    0 0 0 0 3 2;    %Fe2O3 5
    0 1 0 0 4 3;    %FePO4 6
    0 0 1 0 3 0;    %NH3 7
    0 1 1 6 4 0];   %NH4H2PO4 8
                
rank_M = rank(M);
fprintf('r = %d\n', rank_M);

found = 0;
%перебираем комбинации строк по порядку
for i1 = 1:3
    for i2 = i1+1:4
        for i3 = i2+1:5
            for i4 = i3+1:6
                for i5 = i4+1:7
                    for i6 = i5+1:8
                        rows = [i1, i2, i3, i4, i5, i6];
                        subM = M(rows,:);
                        d = det(subM);

                        if abs(d) > 1*10^-5
                            found = found + 1;
                            fprintf('\nподматрица %d (строки [%d,%d,%d,%d,%d,%d]):\n', found, rows);
                            disp(subM);
                            fprintf('определитель = %f\n', d);

                            if found == 2
                                return;
                            end
                        end
                    end
                end
            end
        end
    end
end