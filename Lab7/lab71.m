clear; % Очистить все переменные из памяти MATLAB
clc; % Очистить текст в командном окне
close all; % Закрыть все открытые окна с графиками

% --- 1. Ввод исходных данных из таблицы варианта №17 ---
x = [0, 0.237, 0.417, 0.483, 0.656, 0.777, 0.817, 1.00, 1.15]; % Координаты X узлов
y = [10.0, 10.052, 10.279, 10.836, 11.062, 11.884, 12.019, 13.638, 14.654]; % Значения Y функции
n = length(x); % Считаем общее количество узловых точек
degree = n - 1; % Степень полинома всегда на 1 меньше количества точек
x_check = [0.2, 0.7]; % Точки x1 и x2, в которых нужно найти значения функции

% --- 2. Построение таблицы конечных разностей ---
dy_table = zeros(n, n); % Создаем пустую квадратную матрицу для таблицы разностей
dy_table(:, 1) = y'; % Записываем значения Y в первый столбец матрицы
for j = 2:n % Цикл по столбцам (начиная со второго — это первая разность)
    for i = 1:(n - j + 1) % Цикл по строкам (длина столбцов уменьшается)
        dy_table(i, j) = dy_table(i+1, j-1) - dy_table(i, j-1); % Считаем разность между соседними элементами
    end % Конец цикла по строкам
end % Конец цикла по столбцам

fprintf('Степень интерполирующего полинома: %d\n', degree); % Вывод степени полинома в консоль
fprintf('\nТаблица конечных разностей (фрагмент):\n'); % Вывод заголовка для таблицы
disp(dy_table); % Отображение матрицы разностей в командном окне

% --- 3. Канонический полином через определитель Вандермонда ---
V = zeros(n, n); % Создаем пустую матрицу для матрицы Вандермонда
for i = 1:n % Проход по каждой строке (каждой точке x)
    for j = 1:n % Проход по каждому столбцу (каждой степени)
        V(i, j) = x(i)^(j-1); % Возводим координату x в степень от 0 до n-1
    end % Конец цикла по столбцам
end % Конец цикла по строкам
coeff_vand = V \ y'; % Находим коэффициенты полинома, решая систему линейных уравнений

% --- 4. Интерполяция через стандартные операторы MATLAB (polyfit) ---
[coeff_poly, S, mu] = polyfit(x, y, degree); % Считаем коэффициенты полинома с центрированием (mu)
y_check_poly = polyval(coeff_poly, x_check, S, mu); % Вычисляем значения в точках 0.2 и 0.7 по полиному

% --- 5. Интерполяция сплайнами ---
spline_model = spline(x, y); % Создаем математическую модель кубического сплайна
y_check_spline = ppval(spline_model, x_check); % Вычисляем значения в точках 0.2 и 0.7 по сплайну

% --- 6. Оценка теоретической погрешности ---
max_diff_n = abs(dy_table(1, n)); % Берем последнюю конечную разность для оценки n-й производной
factorial_n = factorial(n); % Вычисляем факториал числа n (для формулы погрешности)
error_estimate = zeros(1, length(x_check)); % Заранее выделяем место под массив погрешностей
for k = 1:length(x_check) % Цикл по точкам, в которых ищем погрешность
    omega = 1; % Начальное значение произведения (x - xi)
    for i = 1:n % Цикл по всем узлам интерполяции
        omega = omega * (x_check(k) - x(i)); % Перемножаем скобки (x - x0)*(x - x1)*...
    end % Конец цикла по узлам
    error_estimate(k) = (max_diff_n / factorial_n) * abs(omega); % Считаем погрешность по формуле
end % Конец цикла по искомым точкам

% --- 7. Визуализация (Построение графиков) ---
x_plot = linspace(min(x), max(x), 1000); % Создаем плотную сетку из 1000 точек для гладкого графика
y_poly_plot = polyval(coeff_poly, x_plot, S, mu); % Считаем значения полинома на этой сетке
y_spline_plot = ppval(spline_model, x_plot); % Считаем значения сплайна на этой сетке
figure('Color', 'w', 'Name', 'Интерполяция для Аркадия'); % Создаем окно графика с белым фоном
subplot(2,1,1); % Разбиваем окно на 2 части, выбираем верхнюю
plot(x_plot, y_poly_plot, 'b-', 'LineWidth', 1.5); % Рисуем линию полинома синим цветом
hold on; % Разрешаем рисовать поверх существующего графика
plot(x, y, 'ro', 'MarkerFaceColor', 'r', 'DisplayName', 'Узлы'); % Рисуем красные точки (узлы)
plot(x_check, y_check_poly, 'gs', 'MarkerSize', 8, 'LineWidth', 2); % Рисуем зеленые квадраты (ответы)
title(['Полиномиальная интерполяция (степень ', num2str(degree), ')']); % Заголовок графика
grid on; % Включаем сетку
legend('Полином', 'Узлы', 'Результат', 'Location', 'best'); % Добавляем легенду (пояснения)
subplot(2,1,2); % Выбираем нижнюю часть окна
plot(x_plot, y_spline_plot, 'm-', 'LineWidth', 1.5); % Рисуем линию сплайна розовым цветом
hold on; % Разрешаем рисование поверх
plot(x, y, 'ro', 'MarkerFaceColor', 'r'); % Рисуем красные узловые точки
plot(x_check, y_check_spline, 'gs', 'MarkerSize', 8, 'LineWidth', 2); % Рисуем ответы сплайна
title('Сплайн-интерполяция (кубическая)'); % Заголовок нижнего графика
grid on; % Включаем сетку
legend('Сплайн', 'Узлы', 'Результат', 'Location', 'best'); % Добавляем легенду

% --- 8. Вывод финальных результатов в консоль ---
fprintf('\nРЕЗУЛЬТАТЫ ДЛЯ АРКАДИЯ:\n'); % Печатаем заголовок итогов
for i = 1:length(x_check) % Цикл для красивого вывода ответов по каждой точке
    fprintf('Точка x%d = %.1f:\n', i, x_check(i)); % Вывод координаты текущей точки
    fprintf('  - Значение (полином): %.4f\n', y_check_poly(i)); % Вывод значения от полинома
    fprintf('  - Значение (сплайн):  %.4f\n', y_check_spline(i)); % Вывод значения от сплайна
    fprintf('  - Оценочная погрешность: %.2e\n', error_estimate(i)); % Вывод погрешности
end % Конец цикла вывода результатов